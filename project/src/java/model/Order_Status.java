package model;

/**
 *
 * @author dell
 */
public class Order_Status {
    private int id;
    private String name;

    public Order_Status() {
    }

    public Order_Status(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Order_Status{" + "id=" + id + ", name=" + name + '}';
    }
    
}
